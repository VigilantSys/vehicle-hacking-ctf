package com.royllsroce.firmware.config;

public class ConfigItem {

    String name;
    String value;

    public ConfigItem(String name, String value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return this.name;
    }

    public String getValue() {
        return this.value;
    }

    public String establish() {
        return this.value;
    }
}
